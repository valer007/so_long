#include "libft.h"

t_list	*ft_lstnew(int content)
{
	t_list	*node;

	node = malloc(sizeof(t_list));
	if (!node)
		return (NULL);
	node->index = 0;
	node->content = content;
	node->prev = NULL;
	return (node);
}
